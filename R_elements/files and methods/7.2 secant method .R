# касательных-хорд (методом секущих)
# Определяем функцию уравнения y = log(x) + x - 0
f <- function(x) log(x) + x 

secant_method <- function(f, x0, x1, tol = 1e-6, max_iter = 100) {
  iter <- 0
  while (abs(x1 - x0) > tol && iter < max_iter) {
    f_x0 <- f(x0)
    f_x1 <- f(x1)
    
    if (f_x1 - f_x0 == 0) {
      stop("Деление на ноль! Метод секущих не сходится.")
    }
    
    # Формула метода секущих (метода хорд)
    x2 <- x1 - f_x1 * (x1 - x0) / (f_x1 - f_x0)
    
    # Обновляем значения для следующей итерации
    x0 <- x1
    x1 <- x2
    iter <- iter + 1
  }
  return(x1)  
}

# 1 Начальные приближения (должны быть близки к корню)
x0 <- 0.1  # Должно быть > 0, так как log(x) не определен для x ≤ 0
x1 <- 2

# Ищем корень
root = secant_method(f, x0, x1)
cat("Приближенное решение уравнения:", root, "\n")
