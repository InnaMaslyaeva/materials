# Написать рекурсивную функцию numberOfWaysMemoization(n), которая:
# если в массиве memoryWays (массив памяти) нет n-ого элемента, 
# то этому элементу нужно глобально присвоить сумму значений этой же функции 
# на двух предыдущих шагах и после вернуть n-ый элемент массива памяти
# 2) задать массив памяти memoryWays с элементарными значениями вне функции
# 3) вызвать функцию numberOfWaysMemoization()

# 1. Создаем глобальный массив памяти с начальными значениями
memoryWays = c(1, 1)  # Количество способов подняться по лестнице с 0 и 1 ступеньками

# 2. Определяем рекурсивную функцию с меморизацией
numberOfWaysMemoization <- function(n) {
  # 2.1 Если значение уже вычислено, возвращаем его
  if (length(memoryWays) >= n + 1 && !is.na(memoryWays[n + 1])) {
    return(memoryWays[n + 1])
  }
  
  # 2.2 Иначе вычисляем его рекурсивно и сохраняем в память
  memoryWays[n + 1] <<- numberOfWaysMemoization(n - 1) + numberOfWaysMemoization(n - 2)
  
  return(memoryWays[n + 1])
}

# 3. Вызываем функцию и выводим результат
n = 10  # Можно изменить значение n
result = numberOfWaysMemoization(n)
cat("Количество способов подняться на", n, "ступенек:", result, "\n")
