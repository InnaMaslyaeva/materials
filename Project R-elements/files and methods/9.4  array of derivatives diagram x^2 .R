# 4.4.Создать функцию dydxCombo(y, h), численно вычисляющую 
# массив производных по массиву значений функции с заданным шагом по следующему правилу:
# 1) в первой точке используется правосторонняя схема;
# 2) в последней точке используется левосторонняя схема;
# 3) в остальных точках используется центральная схема.
# После создать 3 массива относительных ошибок для функции  
# на отрезке  с шагами , , . Построить графики зависимости относительных 
# ошибок от точки вычисления производной на одном рисунке.

# Исходная функция
f <- function(x) {
  return(x^2)  # Можно заменить на другую функцию
}

# Аналитическая производная
df_exact <- function(x) {
  return(x)  # Производная sin(x) — это cos(x)
}

# Функция для вычисления производной комбинированным методом
dydxCombo <- function(y, h) {
  n <- length(y)
  res <- numeric(n)
  
  # 1) Правосторонняя схема для первой точки
  res[1] <- (y[2] - y[1]) / h
  
  # 2) Центральная схема для внутренних точек
  for (i in 2:(n-1)) {
    res[i] <- (y[i+1] - y[i-1]) / (2 * h)
  }
  
  # 3) Левосторонняя схема для последней точки
  res[n] <- (y[n] - y[n-1]) / h
  
  return(res)
}

# Функция для вычисления ошибок
compute_errors <- function(h) {
  x <- seq(0, pi, by = h)  # Отрезок от 0 до pi
  yf <- f(x)               # Вычисляем значения функции
  df_exact_values <- df_exact(x)  # Вычисляем точные значения производной
  
  dy_combo <- dydxCombo(yf, h)  # Численные производные
  
  # Относительная ошибка
  errCombo <- abs((dy_combo - df_exact_values) / df_exact_values) * 100
  return(list(x = x, err = errCombo))
}

# Вычисление ошибок для разных шагов
res1 <- compute_errors(0.1)
res2 <- compute_errors(0.05)
res3 <- compute_errors(0.01)

