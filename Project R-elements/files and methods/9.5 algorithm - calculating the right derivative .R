# Алгоритм вычисления правой производной

# Определяем функцию, для которой будем вычислять производную
y = function(x) {
  return(sqrt(x))  # Функция y = sqrt(x)
}

# Функция вычисления правой производной
dydx = function(y_vals, h) {
  n = length(y_vals)  # Определяем длину массива
  res = numeric(n)  # Создаем массив для производных
  
  for (i in 1:(n-1)) {  # Цикл для вычисления разностной производной
    res[i] = (y_vals[i+1] - y_vals[i]) / h
  }
  res[n] = NA  # Последнее значение пропущено (нет правого соседа)
  
  return(res)
}

# Задаем шаг
h = 0.1

# Создаем массив аргументов x
x = seq(from = 0.1, to = 3, by = h)  

# Вычисляем значения функции
yf = y(x)

# Вычисляем массив значений правой производной
dy = dydx(yf, h)

# Строим график функции
plot(x, yf, type = "l", col = "blue", lwd = 2, ylim = c(0, 2), 
     main = "Функция и её правая производная", ylab = "Значение", xlab = "x")

# Добавляем график правой производной
lines(x, dy, col = "red", lwd = 2)

# Добавляем легенду
legend("topleft", legend = c("Функция y = sqrt(x)", "Правая производная"), 
       col = c("blue", "red"), lwd = 2)
